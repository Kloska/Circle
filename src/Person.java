public class Person {
    public String name;
    public int age;
    public String address;
    public int phoneNumber;
    public int securityNumber;

    public Person(String name, int age, String address, int phoneNumber, int securityNumber) {
        this.name = name;
        this.age = age;
        this.address = address;
        this.phoneNumber = phoneNumber;
        this.securityNumber = securityNumber;
    }
}
